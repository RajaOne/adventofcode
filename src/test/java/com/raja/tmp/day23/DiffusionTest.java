package com.raja.tmp.day23;

import org.junit.jupiter.api.Test;

import static com.raja.tmp.day23.Diffusion.diffusion;
import static org.assertj.core.api.Assertions.assertThat;

class DiffusionTest {

    @Test
    void getScore() {
        var input = """
                .....
                ..##.
                ..#..
                .....
                ..##.
                .....
                """;

        int score = diffusion(input).getScore();

        assertThat(score).isEqualTo(25);
    }

    @Test
    void getScoreLarger() {
        var input = """
                ..............
                ..............
                .......#......
                .....###.#....
                ...#...#.#....
                ....#...##....
                ...#.###......
                ...##.#.##....
                ....#..#......
                ..............
                ..............
                ..............
                """;

        int score = diffusion(input).getScore();

        assertThat(score).isEqualTo(110);
    }

    @Test
    void getScore2() {
        var input = """
                .....
                ..##.
                ..#..
                .....
                ..##.
                .....
                """;

        int score = diffusion(input).getScore2();

        assertThat(score).isEqualTo(4);
    }

    @Test
    void getScoreLarger2() {
        var input = """
                ..............
                ..............
                .......#......
                .....###.#....
                ...#...#.#....
                ....#...##....
                ...#.###......
                ...##.#.##....
                ....#..#......
                ..............
                ..............
                ..............
                """;

        int score = diffusion(input).getScore2();

        assertThat(score).isEqualTo(20);
    }
}
